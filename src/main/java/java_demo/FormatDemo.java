package java_demo;

public class FormatDemo {
    public static void main(String[] args) {
        float num = 3f;
        System.out.printf("num: %.4g %n%n", num);
        System.out.printf("num: %.5g %n%n", num);
        System.out.printf("num: %.6g %n%n", num);
        System.out.printf("num: %.4f %n%n", num);
        System.out.printf("num: %.5f %n%n", num);
        System.out.printf("num: %.6f %n%n", num);

    }
}
